# include <stdio.h>

int main(){
printf("Relational Operators   \n");



printf(" if it print 1 it is ture and if it print 0 it is false\n");


// Equality comparisons
printf("%d\n", 4==4);// Output: 1 (True)
printf("%d\n", 5==7);// Output: 0 (False)

// Inequality comparisons
printf("%d\n", 5!=6);// Output: 1 (True)
printf("%d\n", 3!=3);// Output: 0 (False)

// Greater than comparisons
printf("%d\n", 4>2);// Output: 1 (True)
printf("%d\n", 4>4);// Output: 0 (False)

// Greater than or equal to comparisons
printf("%d\n", 4>=4);// Output: 1 (True)
printf("%d\n", 4>=6);// Output: 0 (False)

// Less than comparisons
printf("%d\n", 3<5);// Output: 1 (True)
printf("%d\n", 3<2);// Output: 0 (False)

// Less than or equal to comparisons
printf("%d\n", 3<=3);// Output: 1 (True)
printf("%d\n", 3<=2);// Output: 0 (False)
  
return 0;
}
